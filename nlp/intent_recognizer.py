class IntentRecognizer:
    def __init__(self):
        # 定义意图关键词映射
        self.intent_keywords = {
            '退款': ['退款', '退钱', '退货', '返还', '退订'],
            '物流': ['物流', '快递', '配送', '发货', '几天到', '什么时候到'],
            '咨询': ['咨询', '询问', '请问', '怎么', '如何', '什么'],
            '优惠': ['优惠', '折扣', '促销', '活动', '便宜'],
            '投诉': ['投诉', '抱怨', '不满意', '生气'],
            '账户': ['账号', '登录', '注册', '密码'],
            # 酒店场景
            '预订': ['预订', '订房', '预定', '订酒店', '预约'],
            '取消': ['取消', '退订', '不要了'],
            '价格': ['价格', '房费', '多少钱', '价位'],
            '服务': ['服务', '设施', '有没有', '提供'],
            # 技术支持场景
            '安装': ['安装', '装不上', '无法安装'],
            '错误': ['错误', '报错', '故障', '问题', '坏了'],
            '使用': ['使用', '怎么用', '操作', '用法'],
            '更新': ['更新', '升级', '新版本'],
            '兼容性': ['兼容', '支持', '能用吗'],
            '问候': ['你好', '您好', 'hello', 'hi']
        }
    
    def recognize_intent(self, user_input):
        """识别用户意图"""
        user_input_lower = user_input.lower()
        
        for intent, keywords in self.intent_keywords.items():
            for keyword in keywords:
                if keyword in user_input_lower:
                    return intent
        
        return '其他'